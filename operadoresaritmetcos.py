# Adição
numero1 = 10
numero2 = 20

soma = numero1 + numero2
print(soma)

# Subtração

subtracao = numero1 - numero2
print(subtracao)

# Multiplicação

multiplicao = numero1 * numero2
print(multiplicao)

#Divisão

divisao = numero1 / numero2
print(divisao)

#Potencia

potencia = numero1 ** numero2
print(potencia)

#Modulo
modulo = numero1 % numero2
print(modulo)

#Resto da divisao
restodivisao = numero1 // numero2
print(restodivisao)